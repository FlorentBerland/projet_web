		<h2>Pourquoi Stellarium?</h2>
		<p>L'astronomie est aujourd'hui un vaste domaine de recherches, de nouvelles étoiles sont découvertes tous les jours. Le problème est que les objets célestes répertoriés sont classés sous des noms différents dans des basses de données différentes sur lesquelles l'accès rapide à l'information n'est pas toujours rapide. Ce site permet offre la possibilité d'accéder aux données grâce à une méthode de navigation en arbre, en partant d'une constellation, soit une zone dans le ciel en précisant la recherche jusqu'à atteindre les valeurs numériques.</p>
